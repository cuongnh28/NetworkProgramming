    private void loadStudent(String keyword){
        ctr = new ClientControl();
        ctr.sendKeyword(keyword);
        List<Student> list = ctr.receiveStudent();
        tmS.setRowCount(0);
        for(Student i:list){
            tmS.addRow(i.toObject());
        }
        ctr.closeConnection();
    }

