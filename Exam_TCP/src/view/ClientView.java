    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ctr = new ClientControl();
        int row = jTable1.getSelectedRow();
        Student student = new Student(Integer.parseInt(jTable1.getValueAt(row, 0).toString()), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText());
//        Student student = new Student(1, jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText());
        ctr.sendStudent(student);
        if(ctr.receiveEdit()){
            JOptionPane.showMessageDialog(this, "Sua thanh cong.");
        }
        else{
            JOptionPane.showMessageDialog(this, "Sua that bai,");
        }
        ctr.closeConnection();
    }//GEN-LAST:event_jButton2ActionPerformed

